package com.java.logicalprogram;

public class VowelOrConsonent {

	public static void main(String[] args) {
         //vowel a,e,i,o,u
		
		
		char ch='b';
		
		if (ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u') {
			System.out.println(ch + " is a vowel");
		}
		else {
			System.out.println(ch + " is a consonent");
		}
	}

}
